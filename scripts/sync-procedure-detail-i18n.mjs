// Add summary to procedure-items from description first sentence
import { readdirSync } from "fs";
const itemsDir = join(__dirname, "../lib/i18n/procedure-items");
for (const file of readdirSync(itemsDir).filter((f) => f.endsWith(".json"))) {
  const data = JSON.parse(readFileSync(join(itemsDir, file), "utf8"));
  for (const item of Object.values(data)) {
    if (!item.summary && item.description) {
      const first = item.description.split(/(?<=[.!?])\s+/)[0] ?? item.description;
      item.summary = first.length > 140 ? `${first.slice(0, 137).trim()}…` : first;
    }
  }
  writeFileSync(join(itemsDir, file), JSON.stringify(data, null, 2) + "\n", "utf8");
}
console.log("Added summary to procedure-items");
