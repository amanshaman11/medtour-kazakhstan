export const categoryLabels: Record<TreatmentCategory, string> = {
  dental: "Dentistry",
  ivf: "IVF",
  cardiology: "Cardiology",
  "plastic-surgery": "Plastic Surgery",
  orthopedics: "Orthopedics",
};

export function searchClinics(
  query: string,
  category?: TreatmentCategory | "all"
): Clinic[] {
  const normalized = query.trim().toLowerCase();

  return clinics.filter((clinic) => {
    const matchesCategory =
      !category || category === "all" || clinic.categories.includes(category);

    if (!normalized) return matchesCategory;

    const searchable = [
      clinic.name,
      clinic.city,
      clinic.specialty,
      ...clinic.categories.map((c) => categoryLabels[c]),
    ]
      .join(" ")
      .toLowerCase();

    return matchesCategory && searchable.includes(normalized);
  });
}
